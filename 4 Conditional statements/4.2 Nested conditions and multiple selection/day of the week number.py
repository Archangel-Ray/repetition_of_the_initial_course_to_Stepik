# Enter the serial number of the day of the week (1, 2, ..., 7).
# Display its name on the screen (Monday, Tuesday, Wednesday, Thursday,
# Friday, Saturday, Sunday). Implement the program using if-elif statements.

num = int(input('a number from 1 to 7: '))
if num == 1:
    print('Monday')
elif num == 2:
    print('Tuesday')
elif num == 3:
    print('Wednesday')
elif num == 4:
    print('Thursday')
elif num == 5:
    print('Friday')
elif num == 6:
    print('Saturday')
elif num == 7:
    print('Sunday')
